package com.example.feastarfeed;

public class Comment {
    String key, content;

    public Comment() {

    }

    public String getContent() {
        return content;
    }

    public String getKey() {
        return key;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
